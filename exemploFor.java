package exemplo;

public class exemploFor {

	public static void main(String[] args) {

		for (int contador = 0; contador < 10; contador++) {
			System.out.println(contador);
		}
		
		System.out.println();
		
		for (int contador = 10; contador > 0 ; contador--) {
			System.out.println(contador);
		}
		
	}

}
